        console.error('‚ùå [M√ìVIL] sessionId recibido:', sessionId);
        console.error('‚ùå [M√ìVIL] Formato esperado: inv_TIMESTAMP_RANDOM');
        mostrarSinInventario('ID de sesi√≥n inv√°lido. Verifique el enlace QR.');
