/**
 * Inicializa la conexi√≥n WebSocket
 */
function inicializarWebSocket() {
    try {
        console.log('üì± [M√ìVIL] ===== INICIANDO CONEXI√ìN WEBSOCKET =====');
        console.log('üì± [M√ìVIL] Timestamp:', new Date().toISOString());
        console.log('üì± [M√ìVIL] Session ID a usar:', sessionId);
        console.log('üì± [M√ìVIL] URL del servidor:', window.location.origin);
        
        // Inicializar socket con reconexi√≥n autom√°tica
        socket = io({
            reconnection: true,
            reconnectionAttempts: 5,
            reconnectionDelay: 1000
        });
        
        socket.on('connect', () => {
            console.log('‚úÖ [M√ìVIL] ===== WEBSOCKET CONECTADO =====');
            console.log('‚úÖ [M√ìVIL] Socket ID asignado:', socket.id);
            console.log('‚úÖ [M√ìVIL] Estado de conexi√≥n:', socket.connected);
            
            // Unirse a la sesi√≥n de inventario
            const datosUnion = { 
                sessionId,
                timestamp: Date.now(),
                userAgent: navigator.userAgent
            };
            
            console.log('üì§ [M√ìVIL] Enviando unirse_inventario:');
            console.log(JSON.stringify(datosUnion, null, 2));
            
            socket.emit('unirse_inventario', datosUnion);
        });

        socket.on('connect_error', (error) => {
            console.error('‚ùå [M√ìVIL] Error de conexi√≥n WebSocket:', error);
            mostrarSinInventario('Error de conexi√≥n con el servidor');
        });
        
        socket.on('conexion_exitosa', (data) => {
            console.log('üéâ [M√ìVIL] ===== CONEXI√ìN EXITOSA =====');
            console.log('üéâ [M√ìVIL] Timestamp:', new Date().toISOString());
            console.log('üéâ [M√ìVIL] Datos completos:', JSON.stringify(data, null, 2));
            
            if (!data || !data.sessionId || !data.usuario) {
                console.error('‚ùå [M√ìVIL] Datos de conexi√≥n incompletos');
                mostrarSinInventario('Error: Datos de conexi√≥n incompletos');
                return;
            }
            
            if (data.sessionId !== sessionId) {
                console.error('‚ùå [M√ìVIL] Session ID no coincide');
                console.error('- Esperado:', sessionId);
                console.error('- Recibido:', data.sessionId);
                mostrarSinInventario('Error: ID de sesi√≥n no coincide');
                return;
            }
            
            conectado = true;
            mostrarFormularioCarga();
            
            // Mostrar el usuario que inici√≥ el inventario
            if (data.usuario && data.usuario.nombre) {
                console.log('üë§ [M√ìVIL] Usuario del inventario:', data.usuario.nombre);
                mostrarUsuarioActivo(`Inventario iniciado por: ${data.usuario.nombre}`);
            } else {
                console.error('‚ùå [M√ìVIL] No hay informaci√≥n de usuario en la respuesta');
                mostrarSinInventario('Error: No hay informaci√≥n de usuario');
                return;
            }
            
            // MOSTRAR INFORMACI√ìN DE SECTORES SI ES INVENTARIO DE INGREDIENTES
            if (esSessionDeIngredientes() && data.sectores) {
                console.log('üè¢ [M√ìVIL] Mostrando informaci√≥n de sectores para ingredientes');
                mostrarInfoSectoresMovil(data.sectores);
            }
            
            console.log('‚úÖ [M√ìVIL] Formulario de carga mostrado y listo');
        });
        
        socket.on('error_conexion', (data) => {
            console.error('‚ùå [M√ìVIL] ERROR DE CONEXI√ìN');
            console.error('‚ùå [M√ìVIL] Mensaje:', data.mensaje);
            console.error('‚ùå [M√ìVIL] Datos completos:', data);
            mostrarSinInventario(data.mensaje || 'Error al conectar con la sesi√≥n');
        });
        
        socket.on('pc_desconectada', () => {
            console.log('‚ö†Ô∏è [M√ìVIL] PC DESCONECTADA');
            console.log('‚ö†Ô∏è [M√ìVIL] Estado anterior conectado:', conectado);
            conectado = false;
            mostrarSinInventario('La PC se ha desconectado. El inventario ha finalizado.');
        });

        socket.on('inventario_finalizado', (data) => {
            console.log('üèÅ [M√ìVIL] INVENTARIO FINALIZADO');
            console.log('üèÅ [M√ìVIL] Datos de finalizaci√≥n:', data);
            conectado = false;
            mostrarSinInventario(data.mensaje || 'El inventario ha finalizado');
        });

        socket.on('articulo_confirmado', (data) => {
            console.log('‚úÖ [M√ìVIL] ART√çCULO CONFIRMADO');
            console.log('‚úÖ [M√ìVIL] Datos de confirmaci√≥n:', data);
            mostrarMensaje(`${data.articulo} registrado: ${data.cantidad}`, 'info');
        });

        socket.on('ingrediente_confirmado', (data) => {
            console.log('‚úÖ [M√ìVIL] INGREDIENTE CONFIRMADO');
            console.log('‚úÖ [M√ìVIL] Datos de confirmaci√≥n:', data);
            mostrarMensaje(`${data.ingrediente} registrado: ${data.cantidad}`, 'info');
        });
        
        socket.on('disconnect', () => {
            console.log('‚ùå [M√ìVIL] DESCONECTADO DE WEBSOCKET');
            console.log('‚ùå [M√ìVIL] Session ID era:', sessionId);
            console.log('‚ùå [M√ìVIL] Socket ID era:', socket.id);
            conectado = false;
            mostrarSinInventario('Conexi√≥n perdida con el servidor');
        });
        
        // Agregar listener para errores generales
        socket.on('error', (error) => {
            console.error('‚ùå [M√ìVIL] Error en WebSocket:', error);
        });
        
        // Agregar listener para eventos no manejados
        socket.onAny((eventName, ...args) => {
            if (!['connect', 'conexion_exitosa', 'error_conexion', 'pc_desconectada', 
                'inventario_finalizado', 'articulo_confirmado', 'disconnect'].includes(eventName)) {
                console.log('üîî [M√ìVIL] Evento WebSocket no manejado:', eventName, args);
            }
        });
        
    } catch (error) {
        console.error('Error al inicializar WebSocket:', error);
        mostrarSinInventario('Error al conectar con el servidor');
    }
}

